// ===== sleep: 趴着睡觉 =====
export const SLEEP_1: number[][] = (() => {
  const frame: number[][] = Array.from({ length: 32 }, () => new Array(32).fill(0))
  // 简化的趴睡姿势 — 身体横躺
  // 头
  for (let y = 18; y <= 22; y++)
    for (let x = 8; x <= 14; x++)
      frame[y][x] = y === 18 || y === 22 ? 1 : (y === 19 ? 2 : 3)
  // 眼睛闭着（横线）
  frame[20][10] = 1; frame[20][11] = 1
  frame[20][13] = 1; frame[20][14] = 1
  // 身体
  for (let y = 20; y <= 24; y++)
    for (let x = 14; x <= 24; x++)
      frame[y][x] = y === 20 || y === 24 ? 8 : 7
  // ZZZ
  frame[16][12] = 6; frame[15][14] = 6; frame[14][16] = 6
  return frame
})()

// ===== bored: 趴着叹气 =====
export const BORED_1: number[][] = (() => {
  const frame = SLEEP_1.map(r => [...r])
  // 去掉 ZZZ，加叹气点点
  frame[16][12] = 0; frame[15][14] = 0; frame[14][16] = 0
  frame[17][16] = 8; frame[16][17] = 8; frame[15][18] = 8 // ...叹气
  return frame
})()

// ===== sulking: 背对着坐 =====
export const SULKING_1: number[][] = (() => {
  const frame: number[][] = Array.from({ length: 32 }, () => new Array(32).fill(0))
  // 头（背面，看不到脸）
  for (let y = 6; y <= 13; y++)
    for (let x = 11; x <= 20; x++)
      frame[y][x] = y <= 8 ? (x === 11 || x === 20 ? 1 : 2) : (x === 11 || x === 20 ? 1 : 1)
  // 身体（背面）
  for (let y = 14; y <= 22; y++)
    for (let x = 10; x <= 21; x++)
      frame[y][x] = x === 10 || x === 21 ? 8 : 7
  // 坐着的腿
  for (let y = 23; y <= 26; y++)
    for (let x = 10; x <= 21; x++)
      frame[y][x] = 13
  return frame
})()

