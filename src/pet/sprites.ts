// ===== idle_stand 第2帧：微微下沉（呼吸感）=====
export const IDLE_STAND_2: number[][] = (() => {
  const frame: number[][] = Array.from({ length: 32 }, () => new Array(32).fill(0))
  for (let y = 0; y < 32; y++) {
    for (let x = 0; x < 32; x++) {
      const srcY = y - 1
      if (srcY >= 0 && srcY < 32) frame[y][x] = IDLE_STAND_1[srcY][x]
    }
  }
  return frame
})()

// ===== happy: 蹦起来（上移3行）=====
export const HAPPY_1: number[][] = (() => {
  const frame: number[][] = Array.from({ length: 32 }, () => new Array(32).fill(0))
  for (let y = 0; y < 32; y++) {
    const srcY = y + 3
    for (let x = 0; x < 32; x++) {
      if (srcY >= 0 && srcY < 32) frame[y][x] = IDLE_STAND_1[srcY][x]
    }
  }
  return frame
})()

// ===== sleep: 趴着睡觉 — 身体横躺 =====
export const SLEEP_1: number[][] = (() => {
  const frame: number[][] = Array.from({ length: 32 }, () => new Array(32).fill(0))
  // 躺着的身体（横向扁平）
  for (let y = 16; y <= 22; y++)
    for (let x = 6; x <= 25; x++)
      frame[y][x] = (y === 16 || y === 22 || x === 6 || x === 25) ? 2 : 1
  // 闭着的眼睛（横线）
  frame[19][10] = 4; frame[19][11] = 4; frame[19][12] = 4
  frame[19][16] = 4; frame[19][17] = 4; frame[19][18] = 4
  // 小短腿（朝下）
  for (let y = 23; y <= 25; y++) {
    frame[y][8] = 5; frame[y][9] = 5
    frame[y][12] = 5; frame[y][13] = 5
    frame[y][18] = 5; frame[y][19] = 5
    frame[y][22] = 5; frame[y][23] = 5
  }
  return frame
})()

// ===== bored: 跟睡觉差不多但没有 ZZZ =====
export const BORED_1: number[][] = SLEEP_1.map(r => [...r])

// ===== sulking: 背对着（身体翻转，没有眼睛）=====
